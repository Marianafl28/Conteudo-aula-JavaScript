// Exercício 9: Escreva um algoritmo para ler as dimensões de um trapezio, calcular e escrever a área do trapezio.

alert("Área do Trapezio");

let base1 = parseFloat(prompt("Informe o valor da base 1: "));
let base2 = parseFloat(prompt("Informe o valor da base 2: "));
let altura = parseFloat(prompt("Informe o valor da altura: "));

let areaTrapezio = ((base1 + base2) * altura) / 2;

alert(`O valor da área do trapezio de base 1 ${base1}, base 2 ${base2} e altura ${altura} é: ${areaTrapezio} m²`);









