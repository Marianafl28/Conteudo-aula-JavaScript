// Exercício 8: Escreva um algoritmo para ler as dimensões de um triangulo, calcular e escrever a área do triangulo.

alert("Área do Triangulo");

let altura = parseFloat(prompt("Informe o valor da altura: "))

let base = parseFloat(prompt("Informe o valor da base: "));

let areaTriangulo = (altura * base) / 2;

alert(`O valor da área do triangulo de altura ${altura} e base ${base} é: ${areaTriangulo} m²`);







